import fs from "node:fs";
import path from "node:path";

const repoRoot = path.resolve(import.meta.dirname, "..");
const localPobMirrorDir = path.join(repoRoot, "local-pob-mirror");
const legionPassivesPath = path.join(localPobMirrorDir, "src", "Data", "TimelessJewelData", "LegionPassives.lua");
const modParserPath = path.join(localPobMirrorDir, "src", "Modules", "ModParser.lua");
const outputPath = path.join(repoRoot, "apps", "web", "lib", "generated", "passive-tree-timeless-keystones.ts");

const legionText = fs.readFileSync(legionPassivesPath, "utf8");
const modParserText = fs.readFileSync(modParserPath, "utf8");

const nodesBlock = extractNamedTable(legionText, "nodes");
const conquerorBlock = extractInlineTable(modParserText, "local conquerorList");

const timelessKeystones = [];

for (const block of parseIndexedBlocks(nodesBlock)) {
  const id = extractLuaString(block, "id");
  if (!id || !id.includes("_keystone_")) {
    continue;
  }

  const name = extractLuaString(block, "dn");
  const icon = normalizeIconPath(extractLuaString(block, "icon"));
  const statDescriptionBlock = extractNamedTable(block, "sd");
  const lines = statDescriptionBlock ? extractLuaStringArray(statDescriptionBlock) : [];

  if (!name) {
    continue;
  }

  timelessKeystones.push({
    icon,
    id,
    lines,
    name,
  });
}

timelessKeystones.sort((left, right) => left.id.localeCompare(right.id));

const conquerorKeystoneIds = new Map();
const conquerorEntryPattern = /\["([^"]+)"\]\s*=\s*\{\s*id\s*=\s*(?:"([^"]+)"|(\d+))\s*,\s*type\s*=\s*"([^"]+)"/g;
let match;
while ((match = conquerorEntryPattern.exec(conquerorBlock)) !== null) {
  const commemoratedName = match[1]?.toLowerCase();
  const id = match[2] ?? match[3];
  const type = match[4];
  if (!commemoratedName || !id || !type) {
    continue;
  }

  const keystoneId = `${type}_keystone_${id}`;
  if (!timelessKeystones.some((entry) => entry.id === keystoneId)) {
    continue;
  }

  conquerorKeystoneIds.set(commemoratedName, keystoneId);
}

const output = [
  "/* This file is generated by scripts/generate-passive-tree-timeless-keystones.mjs - do not edit */",
  "",
  "export interface TimelessKeystoneData {",
  "  name: string;",
  "  icon?: string;",
  "  lines: string[];",
  "}",
  "",
  "export const TIMELESS_KEYSTONE_BY_CONQUEROR_NAME: Record<string, string> = {",
  ...[...conquerorKeystoneIds.entries()]
    .sort(([left], [right]) => left.localeCompare(right))
    .map(([name, keystoneId]) => `  ${JSON.stringify(name)}: ${JSON.stringify(keystoneId)},`),
  "};",
  "",
  "export const TIMELESS_KEYSTONES: Record<string, TimelessKeystoneData> = {",
  ...timelessKeystones.map(
    (entry) =>
      `  ${JSON.stringify(entry.id)}: { name: ${JSON.stringify(entry.name)}, icon: ${JSON.stringify(entry.icon)}, lines: ${JSON.stringify(entry.lines)} },`,
  ),
  "};",
  "",
];

fs.writeFileSync(outputPath, output.join("\n"));

function extractNamedTable(source, key) {
  const pattern = new RegExp(`\\["${escapeRegExp(key)}"\\]\\s*=\\s*\\{`);
  const match = pattern.exec(source);
  if (!match) {
    throw new Error(`Could not find Lua table for "${key}".`);
  }

  const braceIndex = source.indexOf("{", match.index);
  return extractBalancedBlock(source, braceIndex);
}

function extractInlineTable(source, declaration) {
  const declarationIndex = source.indexOf(declaration);
  if (declarationIndex < 0) {
    throw new Error(`Could not find declaration "${declaration}".`);
  }

  const braceIndex = source.indexOf("{", declarationIndex);
  if (braceIndex < 0) {
    throw new Error(`Could not find inline Lua table for "${declaration}".`);
  }

  return extractBalancedBlock(source, braceIndex);
}

function extractBalancedBlock(source, braceIndex) {
  let depth = 0;
  let inString = false;
  let escaped = false;

  for (let index = braceIndex; index < source.length; index += 1) {
    const char = source[index];
    if (inString) {
      if (escaped) {
        escaped = false;
        continue;
      }
      if (char === "\\") {
        escaped = true;
        continue;
      }
      if (char === '"') {
        inString = false;
      }
      continue;
    }

    if (char === '"') {
      inString = true;
      continue;
    }

    if (char === "{") {
      depth += 1;
    } else if (char === "}") {
      depth -= 1;
      if (depth === 0) {
        return source.slice(braceIndex + 1, index);
      }
    }
  }

  throw new Error("Unterminated Lua table block.");
}

function parseIndexedBlocks(source) {
  const blocks = [];
  const pattern = /\[\d+\]\s*=\s*\{/g;
  let match;
  while ((match = pattern.exec(source)) !== null) {
    const braceIndex = source.indexOf("{", match.index);
    blocks.push(extractBalancedBlock(source, braceIndex));
  }
  return blocks;
}

function extractLuaString(source, key) {
  const match = new RegExp(`\\["${escapeRegExp(key)}"\\]\\s*=\\s*"((?:\\\\.|[^"])*)"`).exec(source);
  if (!match) {
    return undefined;
  }
  return JSON.parse(`"${match[1]}"`);
}

function extractLuaStringArray(source) {
  const values = [];
  const pattern = /\[\d+\]\s*=\s*"((?:\\.|[^"])*)"/g;
  let match;
  while ((match = pattern.exec(source)) !== null) {
    values.push(JSON.parse(`"${match[1]}"`));
  }
  return values;
}

function normalizeIconPath(iconPath) {
  if (!iconPath) {
    return undefined;
  }

  return iconPath.replace(/\.dds$/i, ".png");
}

function escapeRegExp(value) {
  return value.replace(/[.*+?^${}()|[\]\\]/g, "\\$&");
}

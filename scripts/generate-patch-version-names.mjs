import fs from "node:fs";
import path from "node:path";

const CHANGELOG_URL = "https://www.pathofexile.com/developer/docs/changelog";
const repoRoot = path.resolve(import.meta.dirname, "..");
const outputPath = path.join(repoRoot, "apps", "web", "lib", "generated", "patch-version-names.ts");

const response = await fetch(CHANGELOG_URL, {
  headers: {
    "User-Agent": "PoB Codes patch-version generator",
  },
});

if (!response.ok) {
  throw new Error(`Failed to fetch ${CHANGELOG_URL} (${response.status})`);
}

const html = await response.text();
const versionNameEntries = extractPatchVersionNames(html);

if (versionNameEntries.length === 0) {
  throw new Error(`Could not parse any PoE1 patch version names from ${CHANGELOG_URL}`);
}

const lines = [
  "/* This file is generated by scripts/generate-patch-version-names.mjs -- do not edit */",
  `/* Source: ${CHANGELOG_URL} */`,
  "",
  "export const PATCH_VERSION_NAMES = {",
  ...versionNameEntries.map(([versionKey, patchName]) => `  ${JSON.stringify(versionKey)}: ${JSON.stringify(patchName)},`),
  "} as const;",
  "",
  "export type PatchVersionKey = keyof typeof PATCH_VERSION_NAMES;",
  "",
];

fs.writeFileSync(outputPath, lines.join("\n"), "utf8");

console.log(`Wrote ${versionNameEntries.length} patch version names to ${outputPath}`);

function extractPatchVersionNames(html) {
  const entries = [];
  const articlePattern = /<article id="3-(\d+)-0">([\s\S]*?)<\/article>/g;

  for (const match of html.matchAll(articlePattern)) {
    const articleMinorVersion = Number(match[1]);
    const articleBody = match[2];
    const versionMatch = articleBody.match(/<h3>\s*(3\.\d+)\.0:\s*<a class="self-link"/);
    const subtitleMatch = articleBody.match(/<div role="doc-subtitle">([\s\S]*?)<\/div>/);

    if (!Number.isFinite(articleMinorVersion) || !versionMatch || !subtitleMatch) {
      continue;
    }

    const versionKey = versionMatch[1];
    const patchName = decodeHtmlEntities(stripTags(subtitleMatch[1])).trim();
    if (!patchName) {
      continue;
    }

    entries.push([versionKey, patchName]);
  }

  entries.sort((left, right) => comparePatchVersionKeys(right[0], left[0]));

  return entries;
}

function comparePatchVersionKeys(left, right) {
  const leftParts = left.split(".").map(Number);
  const rightParts = right.split(".").map(Number);
  const maxLength = Math.max(leftParts.length, rightParts.length);

  for (let index = 0; index < maxLength; index += 1) {
    const difference = (leftParts[index] ?? 0) - (rightParts[index] ?? 0);
    if (difference !== 0) {
      return difference;
    }
  }

  return 0;
}

function stripTags(value) {
  return value.replace(/<[^>]+>/g, "");
}

function decodeHtmlEntities(value) {
  return value
    .replace(/&#(\d+);/g, (_, codePoint) => String.fromCodePoint(Number(codePoint)))
    .replace(/&#x([0-9a-f]+);/gi, (_, codePoint) => String.fromCodePoint(Number.parseInt(codePoint, 16)))
    .replace(/&amp;/g, "&")
    .replace(/&lt;/g, "<")
    .replace(/&gt;/g, ">")
    .replace(/&quot;/g, "\"")
    .replace(/&#39;/g, "'");
}

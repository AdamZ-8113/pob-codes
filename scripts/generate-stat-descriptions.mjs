import fs from "node:fs";
import path from "node:path";

const repoRoot = path.resolve(import.meta.dirname, "..");
const localPobMirrorDir = path.join(repoRoot, "local-pob-mirror");
const statDescDir = path.join(localPobMirrorDir, "src", "Data", "StatDescriptions");
const outputPath = path.join(repoRoot, "apps", "web", "lib", "generated", "stat-descriptions.ts");

// Files to parse, in priority order (later entries win on conflicts)
// gem_stat_descriptions covers support gems, skill_stat_descriptions covers active skill gems
const FILES = [
  "stat_descriptions.lua",
  "skill_stat_descriptions.lua",
  "gem_stat_descriptions.lua",
  "buff_skill_stat_descriptions.lua",
  "aura_skill_stat_descriptions.lua",
  "beam_skill_stat_descriptions.lua",
  "curse_skill_stat_descriptions.lua",
];

/**
 * @type {Map<string, string>} statKey → display template (e.g. "Supported Skills have {0}% increased Attack Speed")
 */
const result = new Map();

for (const file of FILES) {
  const filePath = path.join(statDescDir, file);
  if (!fs.existsSync(filePath)) continue;
  const text = fs.readFileSync(filePath, "utf8");
  parseStatDescFile(text, result);
}

// Write output
const lines = [
  "/* This file is generated by scripts/generate-stat-descriptions.mjs — do not edit */",
  "",
  "// Maps stat key to a display template with {0} placeholder for the stat value.",
  "// Only includes stats with a meaningful display template.",
  "export const STAT_DESCRIPTIONS: Record<string, string> = {",
];

for (const [key, template] of [...result].sort(([a], [b]) => a.localeCompare(b))) {
  lines.push(`  ${JSON.stringify(key)}: ${JSON.stringify(template)},`);
}

lines.push("};", "");
fs.writeFileSync(outputPath, lines.join("\n"), "utf8");
console.log(`Written ${result.size} stat descriptions to ${outputPath}`);

// --- Parser ---

function parseStatDescFile(text, map) {
  // Step 1: Build reverse index from the bottom of the file: ["stat_key"]=N
  const reverseIndex = new Map(); // entryIndex → statKey
  // The reverse index entries look like: ["stat_key"]=246,
  const revRe = /\["([^"]+)"\]\s*=\s*(\d+)/g;
  let rm;
  while ((rm = revRe.exec(text)) !== null) {
    const statKey = rm[1];
    const entryIndex = Number(rm[2]);
    // Only overwrite if not already set (first occurrence wins when multiple keys map to same index)
    if (!reverseIndex.has(entryIndex)) {
      reverseIndex.set(entryIndex, statKey);
    }
    // Also keep all stat keys that point to this entry (we'll handle multi-stat below)
  }

  // Build multi-map: entryIndex → all stat keys pointing to it
  const entryToKeys = new Map();
  const revRe2 = /\["([^"]+)"\]\s*=\s*(\d+)/g;
  while ((rm = revRe2.exec(text)) !== null) {
    const statKey = rm[1];
    const entryIndex = Number(rm[2]);
    if (!entryToKeys.has(entryIndex)) entryToKeys.set(entryIndex, []);
    entryToKeys.get(entryIndex).push(statKey);
  }

  // Step 2: For each top-level entry [N]={...}, extract the display template
  // Top-level entries are indented with exactly one tab: \n\t[N]={
  const entryRe = /\n\t\[(\d+)\]\s*=\s*\{/g;
  let em;
  while ((em = entryRe.exec(text)) !== null) {
    const entryIndex = Number(em[1]);
    const statKeys = entryToKeys.get(entryIndex);
    if (!statKeys || statKeys.length === 0) continue;

    // Extract this entry's block content
    const blockStart = em.index + em[0].length;
    let depth = 1;
    let i = blockStart;
    while (i < text.length && depth > 0) {
      if (text[i] === "{") depth++;
      else if (text[i] === "}") depth--;
      i++;
    }
    const block = text.slice(blockStart, i - 1);

    const template = extractPositiveTemplate(block);
    if (!template) continue;

    // Skip multi-value templates (we can't easily resolve {1})
    if (template.includes("{1}")) continue;

    // Skip templates with no value placeholder (static text only, sometimes useful)
    // We keep them since they describe boolean flags visually

    for (const statKey of statKeys) {
      map.set(statKey, template);
    }
  }
}

/**
 * Extracts the positive-value display template from an entry block.
 * The positive variant is the one with limit [1]=1 (or [1]="#") — always listed first.
 * We just return the FIRST text= occurrence in the block.
 */
function extractPositiveTemplate(block) {
  // Find the first text="..." occurrence
  const textRe = /\btext\s*=\s*"((?:[^"\\]|\\.)*)"/;
  const tm = textRe.exec(block);
  if (!tm) return null;

  // Unescape Lua string escapes (\n → newline, etc.)
  const raw = tm[1].replace(/\\n/g, "\n").replace(/\\"/g, '"').replace(/\\\\/g, "\\");
  return raw;
}
